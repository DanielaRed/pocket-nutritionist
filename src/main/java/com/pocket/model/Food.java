package com.pocket.model;

public class Food {

    private String name;
    private double carbs;
    private double fat;
    private double protein;
    private int calories;

    public Food(String name, double carbs, double fat, double protein, int calories)
    {
        this.name=name;
        this.carbs=carbs;
        this.fat = fat;
        this.protein = protein;
        this.calories = calories;
    }



}
